    private void challenge(String nomeSolicitado) throws RemoteException {
        respostaSol.setVisible(true);
        respostaSol.setText("Aguarde a resposta");
        if (main.challenge(nomeSolicitado)) {
            GamePanelShow();
            setOponentName(nomeSolicitado);
        } else {
        respostaSol.setText("Seu oponente recusou a partida");
        }
    }

    private void AskButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AskButtonActionPerformed
        DefaultTableModel tableAlergias = (DefaultTableModel) jTablePlayers.getModel();
        if (jTablePlayers.getSelectedRow() >= 0 && !tableAlergias.getValueAt(jTablePlayers.getSelectedRow(), 0).equals(myname)) {
            String nomeSolicitacao = (String) tableAlergias.getValueAt(jTablePlayers.getSelectedRow(), 0);

            try {
        challenge(nomeSolicitacao);
        main.setOponent(nomeSolicitacao);
            } catch (RemoteException ex) {
                Logger.getLogger(Game.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um oponente para continuar");
        }
    }//GEN-LAST:event_AskButtonActionPerformed

