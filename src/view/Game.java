    private void adicionaNaTabela(ArrayList<ArrayList> users, ArrayList<ArrayList> teste, DefaultTableModel table) {
        for (int j = 0; j < users.size(); j++) {

            table.addRow(new Object[]{users.get(j).get(0), users.get(j).get(1)});

        }
    }

    private void refresh() {
        ArrayList<ArrayList> users;
        try {
            users = main.getPlayers();
            DefaultTableModel tablePlayers = (DefaultTableModel) jTablePlayers.getModel();
            tablePlayers.setNumRows(0);

            adicionaNaTabela(users, users, tablePlayers);

        } catch (RemoteException ex) {
            Logger.getLogger(Game.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
